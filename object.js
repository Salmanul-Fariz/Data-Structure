///////////////////////////////
////// Keys of Object /////////
///////////////////////////////

const keysOfObject = {
  name: 'user',
  age: 20,
  country: 'india',
};

console.log('------------------------------');
console.log('Keys of Object');
console.log('------------------------------');
console.log(Object.keys(keysOfObject));
// Big-O = O(n)

///////////////////////////////
////// Values of Object ///////
///////////////////////////////

const valuesOfObject = {
  name: 'user',
  age: 20,
  country: 'india',
};

console.log('------------------------------');
console.log('Values of Object');
console.log('------------------------------');
console.log(Object.values(valuesOfObject));
// Big-O = O(n)

///////////////////////////////
///// Entries of Object ///////
///////////////////////////////

const entriesOfObject = {
  name: 'user',
  age: 20,
  country: 'india',
};

console.log('------------------------------');
console.log('Entries of Object');
console.log('------------------------------');
console.log(Object.entries(entriesOfObject));
// Big-O = O(n)
